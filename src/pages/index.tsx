import { type NextPage } from "next";
import Head from "next/head";
import { Navbar } from "~/components/Navbar";
import { Footer } from "~/components/Footer";
import { StudentInformation } from "~/components/StudentInformation";
import { MealSelection } from "~/components/MealSelection";
import { OrderConfirmation } from "~/components/OrderConfirmation";
import { Payment } from "~/components/Payment";
import { useStepStore } from "../state/index";
import { Final } from "~/components/Final";

export const steps = [
  "Student Information",
  "Meal Selection",
  "Order Confirmation",
  "Payment",
  "Done",
];

const Home: NextPage = () => {
  // const [step, setStep] = useState(0);
  const { step } = useStepStore((state) => ({
    step: state.step,
    increment: state.increment,
    decrement: state.decrement,
  }));

  const PageSwitcher: React.FC<{ stepValue: number }> = ({ stepValue }) => {
    return (
      <>
        {stepValue === 0 ? (
          <StudentInformation />
        ) : stepValue === 1 ? (
          <MealSelection />
        ) : stepValue === 2 ? (
          <OrderConfirmation />
        ) : stepValue === 3 ? (
          <Payment />
        ) : stepValue === 4 ? (
          <Final />
        ) : (
          <h2>Nothing</h2>
        )}
      </>
    );
  };

  return (
    <>
      <Head>
        <title>Freshii Ottawa</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <div
        className="flex h-screen  flex-col items-center justify-center "
        data-theme={"autumn"}
      >
        <Navbar />

        {/* Stepper */}
        <div className="my-6 mx-auto">
          <div className="text-center sm:hidden">
            <p>Step {step}</p>
            <p>{steps[step]}</p>
          </div>

          <div className="hidden sm:block">
            <ul className="steps">
              {steps.map((each, idx) => (
                <li
                  key={idx}
                  className={`${step === idx ? "step-accent" : ""} step`}
                >
                  {each}
                </li>
              ))}
            </ul>
          </div>
        </div>

        <main className="relative flex-grow overflow-scroll">
          <PageSwitcher stepValue={step} />
        </main>

        <Footer />
      </div>
    </>
  );
};

export default Home;

